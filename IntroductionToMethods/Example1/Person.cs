namespace Example1
{
    public class Person
    {
        ///////////////////////////////////////////////////////////////////////
        ///////////////////////////////////////////////////////////////////////

        private string _name;       // Private Member Variable of type string.
        private int _age;           // Private Member Variable of type int.

        ///////////////////////////////////////////////////////////////////////
        ///////////////////////////////////////////////////////////////////////

        // Constructor (invoked upon object instantiation)
        // 1.) Same Name As Class
        // 2.) No Return Type

        public Person(string name, int age)
        {
            _name = name;
            _age = age;
        }

        ///////////////////////////////////////////////////////////////////////
        ///////////////////////////////////////////////////////////////////////

        // GetName
        // No Parameters
        // Returns a string

        public string GetName()
        {
            return _name;
        }

        ///////////////////////////////////////////////////////////////////////
        ///////////////////////////////////////////////////////////////////////

        // GetAge
        // No Parameters
        // Returns an int

        public int GetAge()
        {
            return _age;
        }

        ///////////////////////////////////////////////////////////////////////
        ///////////////////////////////////////////////////////////////////////

        // SetName
        // Takes 1 string parameter
        // No Return

        public void SetName(string name)
        {
            _name = name;
        }

        ///////////////////////////////////////////////////////////////////////
        ///////////////////////////////////////////////////////////////////////

        // SetAge
        // Takes 1 integer parameter
        // No Return

        public void SetAge(int age)
        {
            _age = age;
        }

        ///////////////////////////////////////////////////////////////////////
        ///////////////////////////////////////////////////////////////////////
    }
}